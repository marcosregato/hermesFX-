package model;

public class MovimentoBancario {
	
	private String data;
	private String entreda;
	private String saida;
	
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getEntreda() {
		return entreda;
	}
	public void setEntreda(String entreda) {
		this.entreda = entreda;
	}
	public String getSaida() {
		return saida;
	}
	public void setSaida(String saida) {
		this.saida = saida;
	}
}
